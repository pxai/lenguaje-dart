void main () {
  int result = 0;
  int i, j, k;
  i = j = k = 0;
  bool a = true;
  bool b = false;
  bool c;
  c = a && b;     //  false
  c = a || b;     // true
  c = !(a && b);  // true


  print('This program is good: ' + result.toString());
}
